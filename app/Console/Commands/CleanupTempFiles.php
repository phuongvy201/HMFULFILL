    /**
     * Ki·ªÉm tra file c√≥ ƒëang ƒë∆∞·ª£c s·ª≠ d·ª•ng kh√¥ng
     */
    private function isFileInUse($file)
    {
        // Ki·ªÉm tra file c√≥ ƒëang ƒë∆∞·ª£c m·ªü b·ªüi process n√†o kh√¥ng
        $handle = @fopen($file, 'r+');
        if ($handle === false) {
            return true; // File ƒëang ƒë∆∞·ª£c s·ª≠ d·ª•ng
        }
        fclose($handle);
        return false;
    }

    /**
     * L·∫•y t·∫•t c·∫£ files trong directory m·ªôt c√°ch recursive
     */
    private function getFilesRecursively($dir)
    {
        $files = [];
        $iterator = new \RecursiveIteratorIterator(
            new \RecursiveDirectoryIterator($dir, \RecursiveDirectoryIterator::SKIP_DOTS),
            \RecursiveIteratorIterator::LEAVES_ONLY
        );

        foreach ($iterator as $file) {
            if ($file->isFile()) {
                $files[] = $file->getPathname();
            }
        }

        return $files;
    }

    /**
     * Format bytes th√†nh readable string
     */
    private function formatBytes($bytes)
    {
        if ($bytes >= 1024 * 1024 * 1024) {
            return round($bytes / 1024 / 1024 / 1024, 2) . ' GB';
        } elseif ($bytes >= 1024 * 1024) {
            return round($bytes / 1024 / 1024, 2) . ' MB';
        } elseif ($bytes >= 1024) {
            return round($bytes / 1024, 2) . ' KB';
        } else {
            return $bytes . ' B';
        }
    }
