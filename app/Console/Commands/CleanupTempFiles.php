    private function checkDiskSpaceBefore()
    {
        $this->info("1. Ki·ªÉm tra disk space tr∆∞·ªõc khi d·ªçn d·∫πp:");

        $diskFree = disk_free_space(storage_path());
        $diskTotal = disk_total_space(storage_path());
