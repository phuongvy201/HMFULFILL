    /**
     * D·ªçn d·∫πp system temp directory
     */
    private function cleanupSystemTemp($dryRun, $olderThan)
    {
        $this->info("2. D·ªçn d·∫πp system temp directory:");

        $tempDir = sys_get_temp_dir();
        $cutoffTime = time() - ($olderThan * 3600);
        $deletedCount = 0;
        $deletedSize = 0;

        if (!is_dir($tempDir)) {
            $this->warn("   Temp directory kh√¥ng t·ªìn t·∫°i: {$tempDir}");
            return;
        }

        $files = glob($tempDir . '/*');
        $totalFiles = count($files);

        $this->line("   Scanning {$totalFiles} files in {$tempDir}");

        foreach ($files as $file) {
            if (is_file($file)) {
                $fileTime = filemtime($file);
                $fileSize = filesize($file);

                // B·ªè qua files m·ªõi t·∫°o
                if ($fileTime > $cutoffTime) {
                    continue;
                }

                // B·ªè qua files ƒëang ƒë∆∞·ª£c s·ª≠ d·ª•ng
                if ($this->isFileInUse($file)) {
                    continue;
                }

                $deletedCount++;
                $deletedSize += $fileSize;

                if (!$dryRun) {
                    unlink($file);
                }

                $this->line("   " . ($dryRun ? "Would delete" : "Deleted") . ": " . basename($file) . " (" . $this->formatBytes($fileSize) . ")");
            }
        }

        $this->line("   " . ($dryRun ? "Would free" : "Freed") . ": " . $this->formatBytes($deletedSize) . " from {$deletedCount} files");
        $this->info("");
    }

